let a = 10; // = assign a value
console.log("a: ", a);
a += 5; // += add and assign at a same time
console.log("a: ", a);
a -= 5; // -= subtract and assign at a same time
console.log("a: ", a);
a++; // ++ is use to increase by 1
console.log("a: ", a);
a--; // -- is used to decrease by 1
console.log("a: ", a);
a *= 2; // *= multiply and assign at a same time 
console.log("a: ", a);
a /= 2; // /= divide and assign at a same time
console.log("a: ", a);
a %= 2; 
